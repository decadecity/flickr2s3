import unittest
import flickr2s3

import mocks.flickr_api as flickr_api
import mocks.s3_bucket_api as s3_bucket_api

class TestSequenceFunctions(unittest.TestCase):

    def test_parse_jsonp(self):
        self.assertEqual(
            flickr2s3.parse_jsonp('jasonP({ "test_item": "test_value" })'),
            { 'test_item': 'test_value' }
        )

    def test_extract_flickr_ids(self):
        self.assertEqual(
            flickr2s3.extract_flickr_ids([
                { 'id': '8286137174' },
                { 'id': '8286137175' },
                { 'id': '8286137176' },
            ]),
            ['8286137174', '8286137175', '8286137176']
        )

    def test_get_flickr_photos(self):
        self.assertEqual(
            flickr2s3.get_flickr_photos(flickr_api.flickr),
            flickr_photos
        )

    def test_build_flickr_dict(self):
        self.assertEqual(
            flickr2s3.build_flickr_dict(flickr_photos),
            flickr_dict
        )

    def test_extract_filename(self):
        self.assertEqual(
            flickr2s3.extract_filename(u'images/2012-12-15 12.47.32.png'),
            u'2012-12-15 12.47.32.png'
        )
        self.assertEqual(
            flickr2s3.extract_filename(u'images/2012-12-15 12.47.32.jpg'),
            u'2012-12-15 12.47.32.jpg'
        )
        self.assertEqual(
            flickr2s3.extract_filename(u'images/2012-12-15 12.47.32.jpeg'),
            u'2012-12-15 12.47.32.jpeg'
        )

    def test_get_s3_images(self):
        self.assertEqual(
            flickr2s3.get_s3_images(s3_bucket_api.bucket),
            ['8286137174.jpeg', '8286137175.jpeg', '8286137176.jpeg']
        )

    def test_generate_s3_filenames(self):
        self.assertEqual(
            flickr2s3.generate_s3_filename('8286137174', '_o', 'jpeg'),
            '8286137174_o.jpeg'
        )
        self.assertEqual(
            flickr2s3.generate_s3_filename('8286137174', '_o', 'png'),
            '8286137174_o.png'
        )

    def test_get_file_extension(self):
        self.assertEqual(
            flickr2s3.get_file_extension('test.jpeg'),
            'jpeg'
        )
        self.assertEqual(
            flickr2s3.get_file_extension('test.JPEG'),
            'jpeg'
        )
        self.assertEqual(
            flickr2s3.get_file_extension('test.jpg'),
            'jpg'
        )
        self.assertEqual(
            flickr2s3.get_file_extension('test.png.jpeg'),
            'jpeg'
        )
        self.assertEqual(
            flickr2s3.get_file_extension('test.png'),
            'png'
        )
        self.assertRaises(
            AttributeError,
            flickr2s3.get_file_extension,
            'testpng'
        )

    def test_new_extension(self):
        self.assertEqual(
            flickr2s3.new_extension('_o', 'png'),
            'png'
        )
        self.assertEqual(
            flickr2s3.new_extension('_o', 'jpg'),
            'jpeg'
        )
        self.assertEqual(
            flickr2s3.new_extension('_b', 'png'),
            'jpeg'
        )

